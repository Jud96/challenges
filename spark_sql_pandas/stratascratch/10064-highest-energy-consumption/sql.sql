WITH
	FB_ALL_ENERGY AS (
		SELECT
			DATE,
			SUM(CONSUMPTION) AS CONSUMPTION
		FROM
			(
				SELECT
					*
				FROM
					FB_EU_ENERGY
				UNION
				SELECT
					*
				FROM
					FB_ASIA_ENERGY
				UNION
				SELECT
					*
				FROM
					FB_NA_ENERGY
			) ALL_CONSUMPUTION
		GROUP BY
			DATE
	)
SELECT
	DATE,
	CONSUMPTION
FROM
	FB_ALL_ENERGY
WHERE
	CONSUMPTION = (
		SELECT
			MAX(CONSUMPTION) AS MAXENERGY
		FROM
			FB_ALL_ENERGY
	);